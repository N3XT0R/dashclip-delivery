# Videos hochladen und zuweisen

1. **Upload starten:** Klicke im linken Menü auf **Uploads** und verwende die Schaltfläche **Video hinzufügen**. Ziehe deine Datei ins Feld oder wähle sie per Dialog aus.
2. **Metadaten ergänzen:** Beschreibe kurz, was im Video zu sehen ist, und wähle den passenden Kanal. Das System erkennt Dubletten automatisch per SHA-256, damit nichts doppelt landet.
3. **Vorschau prüfen:** Nach dem Upload erzeugt DashClip eine Vorschau und ordnet das Video deinem Kanal zu. Du siehst den Fortschritt direkt in der Liste.
4. **Freigabelink erzeugen:** Gehe zu **Angebote**, wähle das neue Video aus und erstelle einen signierten, zeitlich begrenzten Download-Link – als Einzeldatei oder ZIP.

